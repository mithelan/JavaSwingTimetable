     public ArrayList<NotAvailableSubGrpClass> natretrieve(){
    ArrayList<NotAvailableSubGrpClass> sessionlist=new ArrayList<>();
     try{
           Class.forName("com.mysql.jdbc.Driver");
           
           Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/SPM","root","");
          String query="select * from notavailablesub";
          Statement st=conn.createStatement();
          ResultSet rs=st.executeQuery(query);
          NotAvailableSubGrpClass pd;
          while(rs.next()){
              pd=new NotAvailableSubGrpClass(rs.getInt("id"),rs.getString("subgroup"),rs.getString("Day"),rs.getString("nat"));
              sessionlist.add(pd);
          }
         
          
           
        }catch(Exception e ){
            JOptionPane.showMessageDialog(null,e);
        }
          return sessionlist; 
}
    public void shownat(){
    ArrayList<NotAvailableSubGrpClass> ys=natretrieve();
    DefaultTableModel  model = (DefaultTableModel) natsub.getModel();
    Object[] row =new Object[4];
    for(int i =0;i<ys.size();i++){
        row[0]=ys.get(i).getid();
        row[1]=ys.get(i).getsubgroup();
        row[2]=ys.get(i).getday();
        row[3]=ys.get(i).getnat();
       
       
        model.addRow(row);

                
    }
    
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
